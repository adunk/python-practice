import math

# Constants
print(math.pi)
print(math.e)
print(math.nan)
print(math.inf)

# Trig
obst_direction = math.cos(math.pi / 4)
print(obst_direction)
print(math.sin(math.pi / 4))

# Ceil
cookies = 10.3
candy = 7

print(math.ceil(cookies))
print(math.ceil(candy))

age = 47.9
print(math.floor(age))

# Factorial and square root
i = math.factorial(3)
print(i)
h = math.sqrt(64)
print(h)

# Greatest Common Denominator GCD
# Both equal 4
print(math.gcd(52, 8))
print(math.gcd(8, 52))
# Both equal the same fraction
print(8/52)
print(2/13)

# Degrees and radians
print(math.radians(360))
print(math.pi * 2)
print(math.degrees(math.pi * 2))
